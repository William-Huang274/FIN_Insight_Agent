from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any


REPO_ROOT = Path(__file__).resolve().parents[1]
if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Deterministically repair synthesis citation object_id copy errors against the input evidence pool."
    )
    parser.add_argument(
        "--synthesis-path",
        required=True,
        help="Input synthesis JSON generated by run_calibrated_synthesis_demo.py.",
    )
    parser.add_argument(
        "--output-path",
        required=True,
        help="Output synthesis JSON with deterministic citation repairs applied.",
    )
    parser.add_argument(
        "--repair-report-path",
        required=True,
        help="Audit report for applied and rejected repairs.",
    )
    parser.add_argument(
        "--ledger-path",
        default=None,
        help="Optional Exact-Value Ledger JSON. Enables deterministic metric_id -> object_id citation repair.",
    )
    parser.add_argument(
        "--allow-background-candidates",
        action="store_true",
        help="Allow background evidence ids as repair targets when no citation evidence id matches.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    synthesis = _read_json(REPO_ROOT / args.synthesis_path)
    ledger_rows = _read_json(REPO_ROOT / args.ledger_path).get("rows", []) if args.ledger_path else []
    repaired_results = []
    audit_rows = []
    for result in synthesis.get("results") or []:
        repaired, audit = _repair_result(
            result,
            allow_background=args.allow_background_candidates,
            ledger_rows=ledger_rows,
        )
        repaired_results.append(repaired)
        audit_rows.extend(audit)
    repaired_doc = dict(synthesis)
    repaired_doc["results"] = repaired_results
    repaired_doc["summary"] = _summarize(repaired_results, synthesis.get("summary") or {})
    repaired_doc["repair_report"] = {
        "schema_version": "synthesis_citation_repair_v0.1",
        "source_synthesis_path": str(REPO_ROOT / args.synthesis_path),
        "repair_count": sum(1 for row in audit_rows if row.get("status") == "applied"),
        "rejected_count": sum(1 for row in audit_rows if row.get("status") == "rejected"),
    }

    output_path = REPO_ROOT / args.output_path
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(repaired_doc, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    report = repaired_doc["repair_report"] | {"repairs": audit_rows}
    report_path = REPO_ROOT / args.repair_report_path
    report_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text(json.dumps(report, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(json.dumps(report, ensure_ascii=False, indent=2))


def _repair_result(
    result: dict[str, Any],
    *,
    allow_background: bool,
    ledger_rows: list[dict[str, Any]],
) -> tuple[dict[str, Any], list[dict[str, Any]]]:
    package_metrics = result.get("package_metrics") or {}
    citation_ids = [str(item) for item in package_metrics.get("input_citation_object_ids") or []]
    background_ids = [str(item) for item in package_metrics.get("input_background_object_ids") or []]
    valid_ids = set(citation_ids) | set(background_ids)
    metric_id_repairs = _metric_id_repairs(result, ledger_rows, valid_ids)
    authorized_ledger_rows = _authorized_ledger_rows(result, ledger_rows, valid_ids)
    candidates = citation_ids if citation_ids else []
    if allow_background:
        candidates = candidates + [item for item in background_ids if item not in candidates]
    synthesis = json.loads(json.dumps(result.get("synthesis") or {}, ensure_ascii=False))
    audit_rows: list[dict[str, Any]] = []

    for section_name in ("decision_drivers", "secondary_context", "limiting_caveats"):
        for finding_index, finding in enumerate(synthesis.get(section_name) or [], start=1):
            repaired_ids = []
            for object_id in finding.get("cited_object_ids") or []:
                fixed, audit = _repair_id(str(object_id), candidates, valid_ids, metric_id_repairs)
                audit["location"] = f"{section_name}[{finding_index}].cited_object_ids"
                if audit.get("status") == "rejected" and section_name == "limiting_caveats":
                    audit["status"] = "dropped"
                    audit["reason"] = "dropped_invalid_caveat_citation_id"
                    audit_rows.append(audit)
                    continue
                audit_rows.append(audit)
                repaired_ids.append(fixed)
            finding["cited_object_ids"] = repaired_ids

    for finding_index, finding in enumerate(synthesis.get("key_findings") or [], start=1):
        repaired_ids = []
        for object_id in finding.get("cited_object_ids") or []:
            fixed, audit = _repair_id(str(object_id), candidates, valid_ids, metric_id_repairs)
            audit["location"] = f"key_findings[{finding_index}].cited_object_ids"
            audit_rows.append(audit)
            repaired_ids.append(fixed)
        finding["cited_object_ids"] = repaired_ids

    for claim_index, claim in enumerate(synthesis.get("numeric_claims") or [], start=1):
        metric_id = str(claim.get("metric_id") or "")
        if metric_id in metric_id_repairs:
            claim["cited_object_ids"] = [metric_id_repairs[metric_id]]
        repaired_ids = []
        for object_id in claim.get("cited_object_ids") or []:
            fixed, audit = _repair_id(str(object_id), candidates, valid_ids, metric_id_repairs)
            audit["location"] = f"numeric_claims[{claim_index}].cited_object_ids"
            audit_rows.append(audit)
            repaired_ids.append(fixed)
        claim["cited_object_ids"] = repaired_ids

    for finding_index, finding in enumerate(synthesis.get("facet_findings") or [], start=1):
        repaired_ids = []
        for object_id in finding.get("cited_object_ids") or []:
            fixed, audit = _repair_id(str(object_id), candidates, valid_ids, metric_id_repairs)
            audit["location"] = f"facet_findings[{finding_index}].cited_object_ids"
            audit_rows.append(audit)
            repaired_ids.append(fixed)
        finding["cited_object_ids"] = repaired_ids

    cell_table = synthesis.get("cell_table") or {}
    for cell_index, cell in enumerate(cell_table.get("cells") or [], start=1):
        object_id = cell.get("citation_object_id")
        if object_id:
            fixed, audit = _repair_id(str(object_id), candidates, valid_ids, metric_id_repairs)
            audit["location"] = f"cell_table.cells[{cell_index}].citation_object_id"
            audit_rows.append(audit)
            cell["citation_object_id"] = fixed
        if isinstance(cell.get("citation_object_ids"), list):
            fixed_ids = []
            for id_index, cited_id in enumerate(cell.get("citation_object_ids") or [], start=1):
                fixed, audit = _repair_id(str(cited_id), candidates, valid_ids, metric_id_repairs)
                audit["location"] = f"cell_table.cells[{cell_index}].citation_object_ids[{id_index}]"
                audit_rows.append(audit)
                fixed_ids.append(fixed)
            cell["citation_object_ids"] = fixed_ids

    added_claims = _add_missing_numeric_claims(synthesis, authorized_ledger_rows)
    audit_rows.extend(added_claims)

    repaired = dict(result)
    repaired["synthesis"] = synthesis
    repaired["output_metrics"] = _evaluate_output_use(package_metrics, synthesis)
    return repaired, [row for row in audit_rows if row.get("status") != "unchanged"]


def _metric_id_repairs(
    result: dict[str, Any],
    ledger_rows: list[dict[str, Any]],
    valid_ids: set[str],
) -> dict[str, str]:
    query_id = str(result.get("query_id") or "")
    authorized_metric_ids = {
        str(item)
        for item in (result.get("package_metrics") or {}).get("input_authorized_metric_ids") or []
    }
    repairs: dict[str, str] = {}
    for row in ledger_rows:
        metric_id = str(row.get("metric_id") or "")
        object_id = str(row.get("object_id") or "")
        if not metric_id or not object_id:
            continue
        if query_id and str(row.get("query_id")) != query_id:
            continue
        if authorized_metric_ids and metric_id not in authorized_metric_ids:
            continue
        if object_id not in valid_ids:
            continue
        repairs[metric_id] = object_id
    return repairs


def _authorized_ledger_rows(
    result: dict[str, Any],
    ledger_rows: list[dict[str, Any]],
    valid_ids: set[str],
) -> list[dict[str, Any]]:
    query_id = str(result.get("query_id") or "")
    authorized_metric_ids = {
        str(item)
        for item in (result.get("package_metrics") or {}).get("input_authorized_metric_ids") or []
    }
    rows = []
    seen: set[str] = set()
    for row in ledger_rows:
        metric_id = str(row.get("metric_id") or "")
        object_id = str(row.get("object_id") or "")
        if not metric_id or not object_id:
            continue
        if query_id and str(row.get("query_id")) != query_id:
            continue
        if authorized_metric_ids and metric_id not in authorized_metric_ids:
            continue
        if object_id not in valid_ids:
            continue
        if metric_id in seen:
            continue
        seen.add(metric_id)
        rows.append(row)
    return rows


def _add_missing_numeric_claims(
    synthesis: dict[str, Any],
    authorized_rows: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    existing_metric_ids = {str(claim.get("metric_id") or "") for claim in synthesis.get("numeric_claims") or []}
    by_display: dict[str, list[dict[str, Any]]] = {}
    for row in authorized_rows:
        display = str(row.get("display_value_zh") or "")
        if display:
            by_display.setdefault(display, []).append(row)

    added: list[dict[str, Any]] = []
    for claim_location, text in _prose_fields(synthesis):
        for display, rows in by_display.items():
            if display not in text or len(rows) != 1:
                continue
            row = rows[0]
            metric_id = str(row.get("metric_id") or "")
            if metric_id in existing_metric_ids:
                continue
            synthesis.setdefault("numeric_claims", []).append(
                {
                    "claim_location": claim_location,
                    "metric_id": metric_id,
                    "metric_label_zh": row.get("metric_label") or row.get("metric_family") or "",
                    "raw_value_text": row.get("raw_value_text"),
                    "display_value_zh": row.get("display_value_zh"),
                    "unit": row.get("unit"),
                    "metric_role": row.get("metric_role"),
                    "role_check_zh": row.get("narrative_guard_zh") or "auto-added from authorized ledger row",
                    "cited_object_ids": [row.get("object_id")],
                }
            )
            existing_metric_ids.add(metric_id)
            added.append(
                {
                    "status": "applied",
                    "reason": "missing_numeric_claim_added_from_unique_authorized_display_value",
                    "metric_id": metric_id,
                    "display_value_zh": row.get("display_value_zh"),
                    "location": claim_location,
                }
            )
    return added


def _prose_fields(synthesis: dict[str, Any]) -> list[tuple[str, str]]:
    fields = [
        ("answer", str(synthesis.get("answer_zh") or "")),
        ("thesis", str(synthesis.get("thesis_zh") or "")),
    ]
    for item in synthesis.get("decision_drivers") or []:
        fields.append(("decision_driver", " ".join(str(item.get(key) or "") for key in ("driver_zh", "decision_impact_zh"))))
    for item in synthesis.get("secondary_context") or []:
        fields.append(("secondary_context", " ".join(str(item.get(key) or "") for key in ("context_zh", "why_secondary_zh"))))
    for item in synthesis.get("limiting_caveats") or []:
        fields.append(("limiting_caveat", " ".join(str(item.get(key) or "") for key in ("caveat_zh", "impact_on_thesis_zh"))))
    for item in synthesis.get("facet_findings") or []:
        fields.append(("facet_finding", " ".join(str(item.get(key) or "") for key in ("facet_zh", "takeaway_zh"))))
    return fields


def _repair_id(
    object_id: str,
    candidates: list[str],
    valid_ids: set[str],
    metric_id_repairs: dict[str, str],
) -> tuple[str, dict[str, Any]]:
    if object_id in valid_ids:
        return object_id, {"status": "unchanged", "object_id": object_id}
    if object_id in metric_id_repairs:
        return metric_id_repairs[object_id], {
            "status": "applied",
            "old_object_id": object_id,
            "new_object_id": metric_id_repairs[object_id],
            "reason": "authorized_metric_id_mapped_to_ledger_object_id",
        }
    suffix = _stable_suffix(object_id)
    ticker = object_id.split("_", 1)[0] if "_" in object_id else None
    matches = [
        candidate
        for candidate in candidates
        if _stable_suffix(candidate) == suffix and (ticker is None or candidate.startswith(f"{ticker}_"))
    ]
    if len(matches) == 1:
        return matches[0], {
            "status": "applied",
            "old_object_id": object_id,
            "new_object_id": matches[0],
            "reason": "unique_stable_suffix_match_in_input_citation_pool",
        }
    return object_id, {
        "status": "rejected",
        "old_object_id": object_id,
        "reason": "no_unique_stable_suffix_match",
        "candidate_count": len(matches),
        "candidate_object_ids": matches[:10],
    }


def _stable_suffix(object_id: str) -> str:
    match = re.search(r"_(TABLE|METRIC_TABLE|METRIC_SENT|CLAIM)_[A-Z0-9]+$", object_id)
    if match:
        return match.group(0)
    return object_id.rsplit("_", 1)[-1]


def _evaluate_output_use(package_metrics: dict[str, Any], synthesis: dict[str, Any]) -> dict[str, Any]:
    input_citation_ids = set(package_metrics.get("input_citation_object_ids", []))
    input_background_ids = set(package_metrics.get("input_background_object_ids", []))
    valid_input_ids = input_citation_ids | input_background_ids
    background_only_ids = input_background_ids - input_citation_ids
    cited_ids = _cited_ids_from_synthesis(synthesis)
    cited_set = set(cited_ids)
    return {
        "decision_driver_count": len(synthesis.get("decision_drivers", []) or []),
        "secondary_context_count": len(synthesis.get("secondary_context", []) or []),
        "limiting_caveat_count": len(synthesis.get("limiting_caveats", []) or []),
        "key_finding_count": len(synthesis.get("key_findings", []) or []),
        "facet_finding_count": len(synthesis.get("facet_findings", []) or []),
        "comparability_caveat_count": len(synthesis.get("comparability_caveats_zh", []) or []),
        "numeric_claim_count": len(synthesis.get("numeric_claims", []) or []),
        "cited_object_count": len(cited_set),
        "cited_citation_object_count": len(cited_set & input_citation_ids),
        "cited_background_object_count": len(cited_set & background_only_ids),
        "valid_cited_object_count": len(cited_set & valid_input_ids),
        "invalid_cited_object_ids": sorted(cited_set - valid_input_ids),
        "unused_input_citation_count": len(input_citation_ids - cited_set),
        "missing_aspect_count_in_input": package_metrics.get("missing_aspect_count", 0),
        "model_missing_or_uncertain_count": len(synthesis.get("missing_or_uncertain_zh", []) or [])
        + len(synthesis.get("missing_evidence_by_facet", []) or [])
        + len(synthesis.get("limiting_caveats", []) or []),
    }


def _cited_ids_from_synthesis(synthesis: dict[str, Any]) -> list[str]:
    cited_ids: list[str] = []
    for finding in synthesis.get("decision_drivers", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    for finding in synthesis.get("secondary_context", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    for finding in synthesis.get("limiting_caveats", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    for finding in synthesis.get("key_findings", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    for finding in synthesis.get("facet_findings", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    for finding in synthesis.get("numeric_claims", []) or []:
        cited_ids.extend(str(item) for item in finding.get("cited_object_ids", []) or [] if str(item).strip())
    cell_table = synthesis.get("cell_table") or {}
    for cell in cell_table.get("cells") or []:
        object_ids = list(cell.get("citation_object_ids") or [])
        if cell.get("citation_object_id"):
            object_ids.append(cell.get("citation_object_id"))
        for object_id in object_ids:
            if object_id:
                cited_ids.append(str(object_id))
    return cited_ids


def _summarize(results: list[dict[str, Any]], previous_summary: dict[str, Any]) -> dict[str, Any]:
    summary = dict(previous_summary)
    total_invalid = 0
    total_cited = 0
    total_valid = 0
    total_citation = 0
    for result in results:
        output = result.get("output_metrics") or {}
        total_invalid += len(output.get("invalid_cited_object_ids") or [])
        total_cited += int(output.get("cited_object_count") or 0)
        total_valid += int(output.get("valid_cited_object_count") or 0)
        total_citation += int(output.get("cited_citation_object_count") or 0)
    summary["invalid_cited_object_ids"] = total_invalid
    summary["model_cited_objects"] = total_cited
    summary["model_cited_citation_objects"] = total_citation
    summary["cited_object_precision_against_input"] = round(total_valid / total_cited, 4) if total_cited else 0.0
    return summary


def _read_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


if __name__ == "__main__":
    main()
